class Employee {
    // Employee properties
    firstName: string;
    lastName: string;
    role: string;

    // Employee constructor
    constructor(
        firstName: string,
        lastName: string,
        role: string
    ) {
    
    // Initialize properties of Employee class
    this.firstName - firstName;
    this.lastName = lastName;
    this.role = role
    }
}

// Export the Employee class
export default Employee;